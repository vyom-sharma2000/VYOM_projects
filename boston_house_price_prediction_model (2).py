from sklearn.datasets import load_boston
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

"""DATA PREPROCESSING AND VISUALISATION"""

df = load_boston()
df

df1= pd.DataFrame(df['data'])

df1.columns = df['feature_names']

df1

m= df1.corr()

"""Correlation Matrix """

import seaborn as sns
sns.heatmap(m)

df2= df.target

plt.scatter(df1.TAX,df2)

"""Data Preprocessing"""

from sklearn.preprocessing import StandardScaler

scaler=StandardScaler()

df3=pd.DataFrame(scaler.fit_transform(df1))

df3.columns= df.feature_names

df3

"""MODEL IMPLEMENTATION AND METRICS

"""

from sklearn.linear_model import LinearRegression
from sklearn.metrics import r2_score
from sklearn.model_selection import train_test_split
x_train,x_test,y_train,y_test = train_test_split(df3,df2,test_size=0.2,random_state=2)
model = LinearRegression()
model.fit(x_train,y_train)
y_pred = model.predict(x_test)
r2_score(y_pred,y_test)







