import pandas as pd
from sklearn.datasets import load_breast_cancer

df = load_breast_cancer()
df

"""Data Preprocessing"""

df1 = pd.DataFrame(df.data)
df1

df1.columns = df.feature_names

df2 = pd.DataFrame(df.target)
from sklearn.preprocessing import StandardScaler
scaler = StandardScaler()
df3 = pd.DataFrame(scaler.fit_transform(df1))
df3.columns = df.feature_names
df3

from sklearn.model_selection import train_test_split
x_train,x_test,y_train,y_test = train_test_split(df3,df2, test_size =0.3, random_state = 3)

"""IMPLEMENTATION OF MACHINE LEARNING MODEL AND PERFORMANCE METRICS"""

from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score,precision_score
log_reg = LogisticRegression()
log_reg.fit(x_train,y_train)
y_pred = log_reg.predict(x_test)
precision = precision_score(y_pred,y_test)
accuracy = accuracy_score(y_pred,y_test)







