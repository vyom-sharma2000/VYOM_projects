from sklearn.datasets import load_boston
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

df = load_boston()
df

df1= pd.DataFrame(df['data'])

df1.columns = df['feature_names']

df1

m= df1.corr()

"""Correlation Matrix """

import seaborn as sns
sns.heatmap(m)

df2= df.target

plt.scatter(df1.TAX,df2)

"""Data Preprocessing"""

from sklearn.preprocessing import StandardScaler

scaler=StandardScaler()

df3=pd.DataFrame(scaler.fit_transform(df1))

df3.columns= df.feature_names

df3

from sklearn.linear_model import LinearRegression
from sklearn.model_selection import cross_val_score
from sklearn.model_selection import train_test_split
x_train,x_test,y_train,y_test = train_test_split(df3,df2,test_size=0.2,random_state=2)

"""Performance metrics and Regression"""

lin_reg= LinearRegression()
mse = cross_val_score(lin_reg,x_train,y_train,scoring='r2',cv=5)

print(mse.mean())



